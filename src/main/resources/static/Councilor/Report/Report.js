const session = {
  name: localStorage.getItem('sk_name') || 'Chairman',
  barangay: localStorage.getItem('sk_barangay') || BARANGAY
};

document.getElementById('nameEl').textContent = session.name;

document.getElementById('avatarEl').textContent =
  session.name.charAt(0).toUpperCase();

const yearSel = document.getElementById('reportYear');

const now = new Date();

for (let y = now.getFullYear(); y >= now.getFullYear() - 5; y--) {

  const option = document.createElement('option');

  option.value = y;
  option.textContent = y;

  yearSel.appendChild(option);
}

document.getElementById('reportMonth').value =
  now.getMonth() + 1;

function onTypeChange() {

  const isMonthly =
    document.getElementById('reportType').value === 'monthly';

  document.getElementById('monthGroup').style.display =
    isMonthly ? '' : 'none';
}

async function generateReport() {

  document.getElementById('reportLoading').style.display = 'flex';

  setTimeout(() => {

    document.getElementById('reportLoading').style.display = 'none';

    document.getElementById('reportDoc').style.display = 'block';

    document.getElementById('reportDoc').innerHTML = `
      <div style="padding:2rem">
        <h2>Sample Generated Report</h2>
        <p>Barangay: ${session.barangay}</p>
        <p>Generated by: ${session.name}</p>
      </div>
    `;

    document.getElementById('reportActions').style.display = 'flex';

  }, 1000);
}

function logout() {

  localStorage.removeItem('sk_name');
  localStorage.removeItem('sk_barangay');
  localStorage.removeItem('sk_role');

  window.location.href = '../Log-in/Login';
}